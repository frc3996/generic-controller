typedef struct {
	int8_t left_x;
	int8_t left_y;
	int8_t right_x;
	int8_t right_y;
	uint16_t buttons;

} joystick_report;

joystick_report joystick_report_container;

int adc_conv_done = 1;
volatile int current_channel = 0;
void Next_ADC_Channel()
{
    uint32_t channel;
    current_channel++;
    if (current_channel >= 4) {
    	current_channel = 0;
    }

    switch (current_channel) {
    	case 0:
    		channel = ADC_CHANNEL_1;
    		break;
    	case 1:
    		channel = ADC_CHANNEL_2;
    		break;
    	case 2:
    		channel = ADC_CHANNEL_3;
    		break;
    	case 3:
    		channel = ADC_CHANNEL_4;
    		break;
    	default:
    		channel = ADC_CHANNEL_1;
    		break;
    }

    ADC_ChannelConfTypeDef sConfig = {0};
    // Configure the selected ADC channel
    sConfig.Channel = channel;
    sConfig.Rank = ADC_REGULAR_RANK_1;
    sConfig.SamplingTime = ADC_SAMPLETIME_55CYCLES_5;
    if (HAL_ADC_ConfigChannel(&hadc1, &sConfig) != HAL_OK)
    {
      Error_Handler();
    }

    if (current_channel >= 3) {
		HAL_ADC_Stop_IT(&hadc1);
		adc_conv_done = 1;
	}
}


void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc) {
	int8_t value = (int8_t) (HAL_ADC_GetValue(&hadc1) >> 4) - 128;
	if (value == -128) {
		value = -127;
	}
	switch (current_channel) {
		case 0:
			joystick_report_container.left_x = value;
			break;
		case 1:
			joystick_report_container.left_y = value;
			break;
		case 2:
			joystick_report_container.right_x = value;
			break;
		case 3:
			joystick_report_container.right_y = value;
			break;
		default:
			break;
	}
	Next_ADC_Channel();
}


